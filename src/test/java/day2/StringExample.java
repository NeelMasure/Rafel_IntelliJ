package day2;

public class StringExample {

    public static void main(String[] args) {

        String name = " Hello Neel ";
        System.out.println("TO lower case"+name.toLowerCase());
        System.out.println("TO Upper case"+name.toUpperCase());
        System.out.println("Count off"+name.length());
        System.out.println("Count off name with trim"+name.trim().length());
    }

}
