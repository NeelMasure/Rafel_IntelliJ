package day1;

public class ConditionalOperators {


    public static void main(String[] args) {

        System.out.println(10<20);
        System.out.println(10>20);
        System.out.println(10<=20);
        System.out.println(10>=20);
        System.out.println(10==20); // It works with string and int
        System.out.println("neel"=="Neel");
        String n = "a";
        String N = "A";
        System.out.println("HashCode of a is "+n.hashCode());
        System.out.println("HashCode of a is "+N.hashCode());

    }
}
